  public Configuration() {
    //事务相关的别名配置
    typeAliasRegistry.registerAlias("JDBC", JdbcTransactionFactory.class);
    typeAliasRegistry.registerAlias("MANAGED", ManagedTransactionFactory.class);

    //连接池相关的别名配置
    typeAliasRegistry.registerAlias("JNDI", JndiDataSourceFactory.class);
    typeAliasRegistry.registerAlias("POOLED", PooledDataSourceFactory.class);
    typeAliasRegistry.registerAlias("UNPOOLED", UnpooledDataSourceFactory.class);

    //缓冲相关的别名配置
    //不支持多线程，因为没能实现接口的锁功能，而且内部的map不是并发的map
    //缓存的功能是由内部的map结构来实现的．
    typeAliasRegistry.registerAlias("PERPETUAL", PerpetualCache.class);

    //列队形式缓冲
    //使用委派的设计模式，本省不保存缓冲数据，只保存缓冲的key，正常保存的缓冲数据在委派的Cache实现中，
    //这里主要通过列队的形式保证缓冲的个数恒定，默认为1024个。
    typeAliasRegistry.registerAlias("FIFO", FifoCache.class);

    //和FifoCache缓冲用到的设计模式一致，采用委派设计模式(这里翻译使用的是装饰者的设计模式
    //这个实现主要通过最近使用的思路实现缓冲的个数恒定，默认个数也是1024个
    //这里用到LinkedHashMap实现。详细解释可以查看具体实现类
    typeAliasRegistry.registerAlias("LRU", LruCache.class);

    //软引用的缓冲，JVM中对于引用有四种形式，强引用(默认)、软应用、弱引用、虚引用
    //引用类型	        用途	        被回收时间	        生存时间
    // 强引用	     对象的一般状态	     从来不会	      JVM停止运行时终止
    // 软引用	        对象缓存	       内存不足	        内存不足时终止
    // 弱引用	        对象缓存	       垃圾回收       	  GC运行后终止
    // 虚引用           无            必定会回收
    //“虚引用”顾名思义，就是形同虚设，与其他几种引用都不同，虚引用并不会决定对象的生命周期。
    // 如果一个对象仅持有虚引用，那么它就和没有任何引用一样，在任何时候都可能被垃圾回收器回收。
    //虚引用主要用来跟踪对象被垃圾回收器回收的活动。虚引用与软引用和弱引用的一个区别在于：
    // 虚引用必须和引用队列（ReferenceQueue）联合使用。当垃圾回收器准备回收一个对象时，
    // 如果发现它还有虚引用，就会在回收对象的内存之前，把这个虚引用加入到与之关联的引用队列中。
    //这里的SOFT为软引用，WEAK为弱引用
    typeAliasRegistry.registerAlias("SOFT", SoftCache.class);
    typeAliasRegistry.registerAlias("WEAK", WeakCache.class);

    //供应商数据库ID提供程序
    typeAliasRegistry.registerAlias("DB_VENDOR", VendorDatabaseIdProvider.class);

    //Xml解析相关的别名初始化
    typeAliasRegistry.registerAlias("XML", XMLLanguageDriver.class);
    typeAliasRegistry.registerAlias("RAW", RawLanguageDriver.class);

    //日志相关的别名初始化
    typeAliasRegistry.registerAlias("SLF4J", Slf4jImpl.class);
    typeAliasRegistry.registerAlias("COMMONS_LOGGING", JakartaCommonsLoggingImpl.class);
    typeAliasRegistry.registerAlias("LOG4J", Log4jImpl.class);
    typeAliasRegistry.registerAlias("LOG4J2", Log4j2Impl.class);
    typeAliasRegistry.registerAlias("JDK_LOGGING", Jdk14LoggingImpl.class);
    typeAliasRegistry.registerAlias("STDOUT_LOGGING", StdOutImpl.class);
    typeAliasRegistry.registerAlias("NO_LOGGING", NoLoggingImpl.class);

    //cglib相关的别名初始化
    typeAliasRegistry.registerAlias("CGLIB", CglibProxyFactory.class);
    typeAliasRegistry.registerAlias("JAVASSIST", JavassistProxyFactory.class);

    languageRegistry.setDefaultDriverClass(XMLLanguageDriver.class);
    languageRegistry.register(RawLanguageDriver.class);
  }

